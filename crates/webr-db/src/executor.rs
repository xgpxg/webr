/// Generate a `bind` method for a binder with generic params `<'q, R>`.
macro_rules! __bind_qr {
    ($binder:ident, $DB:ty, $Args:ty, $variant:ident) => {
        impl<'q, R: Send + Unpin> $binder<'q, R> {
            pub fn bind<T>(self, value: T) -> Self
            where
                T: sqlx::Encode<'q, $DB> + sqlx::Type<$DB> + Send + 'q,
            {
                match self {
                    Self::$variant(q) => Self::$variant(q.bind(value)),
                    #[allow(unreachable_patterns)]
                    _ => self,
                }
            }
        }
    };
}

/// Generate a `bind` method for `ExecutionBinder` (only `<'q>`, no `R`).
macro_rules! __bind_exec {
    ($DB:ty, $Args:ty, $variant:ident) => {
        impl<'q> ExecutionBinder<'q> {
            pub fn bind<T>(self, value: T) -> Self
            where
                T: sqlx::Encode<'q, $DB> + sqlx::Type<$DB> + Send + 'q,
            {
                match self {
                    Self::$variant(q) => Self::$variant(q.bind(value)),
                    #[allow(unreachable_patterns)]
                    _ => self,
                }
            }
        }
    };
}

// QueryBinder bind — priority: pg > mysql > sqlite
#[cfg(feature = "postgres")]
__bind_qr!(QueryBinder, sqlx::Postgres, sqlx::postgres::PgArguments, Postgres);
#[cfg(all(feature = "mysql", not(feature = "postgres")))]
__bind_qr!(QueryBinder, sqlx::MySql, sqlx::mysql::MySqlArguments, MySql);
#[cfg(all(feature = "sqlite", not(feature = "postgres"), not(feature = "mysql")))]
__bind_qr!(QueryBinder, sqlx::Sqlite, sqlx::sqlite::SqliteArguments<'q>, Sqlite);

// ScalarBinder bind
#[cfg(feature = "postgres")]
__bind_qr!(ScalarBinder, sqlx::Postgres, sqlx::postgres::PgArguments, Postgres);
#[cfg(all(feature = "mysql", not(feature = "postgres")))]
__bind_qr!(ScalarBinder, sqlx::MySql, sqlx::mysql::MySqlArguments, MySql);
#[cfg(all(feature = "sqlite", not(feature = "postgres"), not(feature = "mysql")))]
__bind_qr!(ScalarBinder, sqlx::Sqlite, sqlx::sqlite::SqliteArguments<'q>, Sqlite);

// ExecutionBinder bind
#[cfg(feature = "postgres")]
__bind_exec!(sqlx::Postgres, sqlx::postgres::PgArguments, Postgres);
#[cfg(all(feature = "mysql", not(feature = "postgres")))]
__bind_exec!(sqlx::MySql, sqlx::mysql::MySqlArguments, MySql);
#[cfg(all(feature = "sqlite", not(feature = "postgres"), not(feature = "mysql")))]
__bind_exec!(sqlx::Sqlite, sqlx::sqlite::SqliteArguments<'q>, Sqlite);

