type BoxFut<'a, T> = Pin<Box<dyn Future<Output = T> + Send + 'a>>;

/// Generate `query_as`, `query_scalar`, `query_exec` methods.
///
/// Two forms:
///   pool:  `__query_buildors!(pool  { ... })` — matches on `&self.inner`
///   txn:   `__query_buildors!(driver { ... })` — matches on `self.driver()`
///
/// Each arm: `$(#[$cfg])? $pat => $Variant`
macro_rules! __query_buildors {
    (pool { $( $(#[$cfg:meta])? $pat:pat => $Variant:ident, )+ }) => {
        #[allow(unused_variables)]
        pub fn query_as<'q, R>(&'q self, sql: &'q str) -> QueryBinder<'q, R>
        where
            R: crate::Row,
        {
            match &self.inner {
                $( $(#[$cfg])? $pat => QueryBinder::$Variant(sqlx::query_as::<sqlx::$Variant, R>(sql)), )+
                #[allow(unreachable_patterns)]
                _ => unreachable!("no database feature enabled"),
            }
        }

        #[allow(unused_variables)]
        pub fn query_scalar<'q, T: crate::Scalar>(&'q self, sql: &'q str) -> ScalarBinder<'q, T> {
            match &self.inner {
                $( $(#[$cfg])? $pat => ScalarBinder::$Variant(sqlx::query_scalar::<sqlx::$Variant, T>(sql)), )+
                #[allow(unreachable_patterns)]
                _ => unreachable!("no database feature enabled"),
            }
        }

        pub fn query_exec<'q>(&'q self, sql: &'q str) -> ExecutionBinder<'q> {
            match &self.inner {
                $( $(#[$cfg])? $pat => ExecutionBinder::$Variant(sqlx::query(sql)), )+
                #[allow(unreachable_patterns)]
                _ => unreachable!("no database feature enabled"),
            }
        }
    };
    (driver { $( $(#[$cfg:meta])? $pat:pat => $Variant:ident, )+ }) => {
        #[allow(unused_variables)]
        pub fn query_as<'q, R>(&'q self, sql: &'q str) -> QueryBinder<'q, R>
        where
            R: crate::Row,
        {
            match self.driver() {
                $( $(#[$cfg])? $pat => QueryBinder::$Variant(sqlx::query_as::<sqlx::$Variant, R>(sql)), )+
                #[allow(unreachable_patterns)]
                _ => unreachable!("no database feature enabled"),
            }
        }

        #[allow(unused_variables)]
        pub fn query_scalar<'q, T: crate::Scalar>(&'q self, sql: &'q str) -> ScalarBinder<'q, T> {
            match self.driver() {
                $( $(#[$cfg])? $pat => ScalarBinder::$Variant(sqlx::query_scalar::<sqlx::$Variant, T>(sql)), )+
                #[allow(unreachable_patterns)]
                _ => unreachable!("no database feature enabled"),
            }
        }

        pub fn query_exec<'q>(&'q self, sql: &'q str) -> ExecutionBinder<'q> {
            match self.driver() {
                $( $(#[$cfg])? $pat => ExecutionBinder::$Variant(sqlx::query(sql)), )+
                #[allow(unreachable_patterns)]
                _ => unreachable!("no database feature enabled"),
            }
        }
    };
}

